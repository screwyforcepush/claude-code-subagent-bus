<template>
  <Teleport to="body">
    <div v-if="isOpen" class="fixed inset-0 z-50 flex items-center justify-center p-4 mobile:p-0">
      <!-- Backdrop -->
      <div 
        class="fixed inset-0 bg-black/75 transition-opacity"
        @click="close"
      ></div>
      
      <!-- Modal -->
      <div 
        class="relative diablo-panel border border-diablo-blood/50 rounded-lg mobile:rounded-none shadow-2xl flex flex-col overflow-hidden z-10 mobile:w-full mobile:h-full mobile:fixed mobile:inset-0"
        :style="{ width: '85vw', height: '85vh' }"
        :class="{ 'mobile:!w-full mobile:!h-full': true }"
        @click.stop
      >
          <!-- Header -->
          <div class="flex-shrink-0 diablo-header border-b border-diablo-blood/45 p-6 mobile:p-3">
            <div class="flex items-center justify-between mb-4 mobile:mb-2">
              <h2 class="text-3xl mobile:text-lg font-diablo font-semibold text-diablo-gold tracking-[0.2em] uppercase drop-shadow-[0_2px_10px_rgba(214,168,96,0.55)]">
                💬 Chat Transcript
              </h2>
              <button
                @click="close"
                class="p-2 mobile:p-3 rounded-lg transition-all duration-150 min-w-[44px] min-h-[44px] flex items-center justify-center bg-diablo-900/70 hover:bg-diablo-850/80 border border-diablo-ash/70 hover:border-diablo-brass/60"
              >
                <svg class="w-6 h-6 mobile:w-5 mobile:h-5 text-diablo-parchment/70" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12" />
                </svg>
              </button>
            </div>
            
            <!-- Search and Filters -->
            <div class="space-y-4">
              <!-- Search Input -->
              <div class="flex gap-2">
                <div class="relative flex-1">
                  <input
                    v-model="searchQuery"
                    @keyup.enter="executeSearch"
                    type="text"
                    placeholder="Search transcript..."
                    class="w-full px-4 py-2 mobile:px-3 mobile:py-2 pl-10 mobile:pl-8 text-lg mobile:text-base rounded-lg diablo-field focus:ring-2 focus:ring-[rgba(214,168,96,0.25)] focus:border-[rgba(214,168,96,0.6)] placeholder:text-diablo-parchment/40"
                  >
                  <svg class="absolute left-3 top-3 w-5 h-5 text-diablo-parchment/50" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z" />
                  </svg>
                </div>
                <button
                  @click="executeSearch"
                  class="px-4 py-2 mobile:px-3 mobile:py-2 font-semibold rounded-lg transition-all text-base mobile:text-sm min-w-[44px] min-h-[44px] flex items-center justify-center diablo-button"
                >
                  Search
                </button>
                <button
                  @click="copyAllMessages"
                  class="px-4 py-2 mobile:px-3 mobile:py-2 bg-diablo-ash/70 hover:bg-diablo-ash text-diablo-parchment font-semibold rounded-lg transition-all text-base mobile:text-sm min-w-[44px] min-h-[44px] flex items-center justify-center border border-diablo-ash/80 hover:border-diablo-brass/50"
                  title="Copy all messages as JSON"
                >
                  {{ copyAllButtonText }}
                </button>
              </div>
              
              <!-- Filters -->
              <div class="flex flex-wrap gap-2 mobile:gap-1 max-h-24 mobile:max-h-32 overflow-y-auto p-2 mobile:p-1 bg-diablo-900/70 border border-diablo-blood/40 rounded-lg mobile:overflow-x-auto mobile:pb-2">
                <button
                  v-for="filter in filters"
                  :key="filter.type"
                  @click="toggleFilter(filter.type)"
                  class="px-4 py-2 mobile:px-3 mobile:py-1.5 rounded-full text-sm mobile:text-xs font-semibold transition-all min-h-[44px] mobile:min-h-[36px] flex items-center whitespace-nowrap uppercase tracking-wide"
                  :class="activeFilters.includes(filter.type) 
                    ? 'bg-diablo-blood text-diablo-parchment border border-diablo-brass/60 shadow-[0_0_12px_rgba(176,30,33,0.4)]' 
                    : 'bg-diablo-850/80 text-diablo-parchment/70 border border-diablo-ash/50 hover:bg-diablo-850 hover:text-diablo-gold hover:border-diablo-brass/50'"
                >
                  <span class="mr-1">{{ filter.icon }}</span>
                  {{ filter.label }}
                </button>
                
                <!-- Clear Filters -->
                <button
                  v-if="searchQuery || activeSearchQuery || activeFilters.length > 0"
                  @click="clearSearch"
                  class="px-4 py-2 mobile:px-3 mobile:py-1.5 rounded-full text-sm mobile:text-xs font-semibold bg-diablo-ash/60 text-diablo-parchment hover:bg-diablo-ash min-h-[44px] mobile:min-h-[36px] flex items-center whitespace-nowrap border border-diablo-blood/50"
                >
                  Clear All
                </button>
              </div>
              
              <!-- Results Count -->
              <div v-if="activeSearchQuery || activeFilters.length > 0" class="text-sm mobile:text-xs text-diablo-parchment/60">
                Showing {{ filteredChat.length }} of {{ chat.length }} messages
                <span v-if="activeSearchQuery" class="ml-2 font-medium mobile:block mobile:ml-0 mobile:mt-1">
                  (searching for "{{ activeSearchQuery }}")
                </span>
              </div>
            </div>
          </div>
          
          <!-- Content -->
          <div class="flex-1 p-6 mobile:p-3 overflow-hidden flex flex-col bg-diablo-900/60">
            <ChatTranscript :chat="filteredChat" />
          </div>
        </div>
    </div>
  </Teleport>
</template>

<script setup lang="ts">
import { ref, computed, watch, onMounted, onUnmounted } from 'vue';
import ChatTranscript from './ChatTranscript.vue';

const props = defineProps<{
  isOpen: boolean;
  chat: any[];
}>();

const emit = defineEmits<{
  close: [];
}>();

const searchQuery = ref('');
const activeSearchQuery = ref('');
const activeFilters = ref<string[]>([]);
const copyAllButtonText = ref('📋 Copy All');

const filters = [
  // Message types
  { type: 'user', label: 'User', icon: '👤' },
  { type: 'assistant', label: 'Assistant', icon: '🤖' },
  { type: 'system', label: 'System', icon: '⚙️' },
  
  // Tool actions
  { type: 'tool_use', label: 'Tool Use', icon: '🔧' },
  { type: 'tool_result', label: 'Tool Result', icon: '✅' },
  
  // Specific tools
  { type: 'Read', label: 'Read', icon: '📄' },
  { type: 'Write', label: 'Write', icon: '✍️' },
  { type: 'Edit', label: 'Edit', icon: '✏️' },
  { type: 'Glob', label: 'Glob', icon: '🔎' },
];

const toggleFilter = (type: string) => {
  const index = activeFilters.value.indexOf(type);
  if (index > -1) {
    activeFilters.value.splice(index, 1);
  } else {
    activeFilters.value.push(type);
  }
};

const executeSearch = () => {
  activeSearchQuery.value = searchQuery.value;
};

const clearSearch = () => {
  searchQuery.value = '';
  activeSearchQuery.value = '';
  activeFilters.value = [];
};

const close = () => {
  emit('close');
};

const copyAllMessages = async () => {
  try {
    // Copy all chat messages as formatted JSON
    const jsonPayload = JSON.stringify(props.chat, null, 2);
    await navigator.clipboard.writeText(jsonPayload);
    
    copyAllButtonText.value = '✅ Copied!';
    setTimeout(() => {
      copyAllButtonText.value = '📋 Copy All';
    }, 2000);
  } catch (err) {
    console.error('Failed to copy all messages:', err);
    copyAllButtonText.value = '❌ Failed';
    setTimeout(() => {
      copyAllButtonText.value = '📋 Copy All';
    }, 2000);
  }
};

const matchesSearch = (item: any, query: string): boolean => {
  const lowerQuery = query.toLowerCase().trim();
  
  // Check direct content (for system messages and simple chat)
  if (typeof item.content === 'string') {
    // Remove ANSI codes before searching
    const cleanContent = item.content.replace(/\u001b\[[0-9;]*m/g, '').toLowerCase();
    if (cleanContent.includes(lowerQuery)) {
      return true;
    }
  }
  
  // Check role in simple format
  if (item.role && item.role.toLowerCase().includes(lowerQuery)) {
    return true;
  }
  
  // Check message object (complex format)
  if (item.message) {
    // Check message role
    if (item.message.role && item.message.role.toLowerCase().includes(lowerQuery)) {
      return true;
    }
    
    // Check message content
    if (item.message.content) {
      if (typeof item.message.content === 'string' && item.message.content.toLowerCase().includes(lowerQuery)) {
        return true;
      }
      // Check array content
      if (Array.isArray(item.message.content)) {
        for (const content of item.message.content) {
          if (content.text && content.text.toLowerCase().includes(lowerQuery)) {
            return true;
          }
          if (content.name && content.name.toLowerCase().includes(lowerQuery)) {
            return true;
          }
          if (content.input && JSON.stringify(content.input).toLowerCase().includes(lowerQuery)) {
            return true;
          }
          if (content.content && typeof content.content === 'string' && content.content.toLowerCase().includes(lowerQuery)) {
            return true;
          }
        }
      }
    }
  }
  
  // Check type
  if (item.type && item.type.toLowerCase().includes(lowerQuery)) {
    return true;
  }
  
  // Check parentUuid, uuid, sessionId
  if (item.uuid && item.uuid.toLowerCase().includes(lowerQuery)) {
    return true;
  }
  if (item.sessionId && item.sessionId.toLowerCase().includes(lowerQuery)) {
    return true;
  }
  
  // Check toolUseResult
  if (item.toolUseResult) {
    if (JSON.stringify(item.toolUseResult).toLowerCase().includes(lowerQuery)) {
      return true;
    }
  }
  
  return false;
};

const matchesFilters = (item: any): boolean => {
  if (activeFilters.value.length === 0) return true;
  
  // Check message type
  if (item.type && activeFilters.value.includes(item.type)) {
    return true;
  }
  
  // Check role (simple format)
  if (item.role && activeFilters.value.includes(item.role)) {
    return true;
  }
  
  // Check for system messages with hook types
  if (item.type === 'system' && item.content) {
    // Extract hook type from system content (e.g., "PreToolUse:Read")
    const hookMatch = item.content.match(/([A-Za-z]+):/)?.[1];
    if (hookMatch && activeFilters.value.includes(hookMatch)) {
      return true;
    }
    // Also check if content contains "Running"
    if (item.content.includes('Running') && activeFilters.value.includes('Running')) {
      return true;
    }
    // Check for specific tool names in system messages
    const toolNames = ['Read', 'Write', 'Edit', 'Glob'];
    for (const tool of toolNames) {
      if (item.content.includes(tool) && activeFilters.value.includes(tool)) {
        return true;
      }
    }
  }
  
  // Check for command messages
  if (item.message?.content && typeof item.message.content === 'string') {
    if (item.message.content.includes('<command-') && activeFilters.value.includes('command')) {
      return true;
    }
  }
  
  // Check for meta messages
  if (item.isMeta && activeFilters.value.includes('meta')) {
    return true;
  }
  
  // Check for tool use in content
  if (item.message?.content && Array.isArray(item.message.content)) {
    for (const content of item.message.content) {
      if (content.type === 'tool_use') {
        if (activeFilters.value.includes('tool_use')) {
          return true;
        }
        // Check for specific tool names
        if (content.name && activeFilters.value.includes(content.name)) {
          return true;
        }
      }
      if (content.type === 'tool_result' && activeFilters.value.includes('tool_result')) {
        return true;
      }
    }
  }
  
  return false;
};

const filteredChat = computed(() => {
  if (!activeSearchQuery.value && activeFilters.value.length === 0) {
    return props.chat;
  }
  
  return props.chat.filter(item => {
    const matchesQueryCondition = !activeSearchQuery.value || matchesSearch(item, activeSearchQuery.value);
    const matchesFilterCondition = matchesFilters(item);
    return matchesQueryCondition && matchesFilterCondition;
  });
});

// Handle ESC key
const handleKeydown = (e: KeyboardEvent) => {
  if (e.key === 'Escape' && props.isOpen) {
    close();
  }
};

onMounted(() => {
  document.addEventListener('keydown', handleKeydown);
});

onUnmounted(() => {
  document.removeEventListener('keydown', handleKeydown);
});

// Reset search when modal closes
watch(() => props.isOpen, (newVal) => {
  if (!newVal) {
    clearSearch();
  }
});

</script>
